package com.fang.character;

public class MyCharacter {
    public static void main(String[] args) {
        char c1 = (char) 8;
        char c2 = (char) 9;
        char c3 = (char) 10;
        char c4 = (char) 13;
        char c5 = (char) 32;
        char c6 = (char) 160;
        char c7 = (char) 12288;
        System.out.println("char = 8 : " + c1);
        System.out.println("char = 9 : " + c2);
        System.out.println("char = 10 : " + c3);
        System.out.println("char = 13 : " + c4);
        System.out.println("char = 32 : " + c5);
        System.out.println("char = 160 : " + c6);
        System.out.println("char = 12288 : " + c7);
    }
}
